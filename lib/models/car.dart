class Car {
  final String brand;
  final String model;
  final String type;
  final int yearMade;
  final String color;
  final int kilometerDriven;
  final String fuelType;
  final DateTime insuranceExpiry;
  final DateTime nextTechnicalCheck;

  Car({
    required this.brand,
    required this.model,
    required this.type,
    required this.yearMade,
    required this.color,
    required this.kilometerDriven,
    required this.fuelType,
    required this.insuranceExpiry,
    required this.nextTechnicalCheck,
  });
}
